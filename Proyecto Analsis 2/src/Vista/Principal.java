    private LinkedList<String> listarVariablesRegistro(HashMap<String, String> variables) {
        LinkedList<String> vars = new LinkedList<>();
        for (String llave : variables.keySet()) {
            vars.add(llave + " = " + variables.get(llave));
        }
        return vars;
    }
    
